export const getExecutivePositions = () => EXECUTIVE_TEAM.positions;

export const getDepartmentById = (id) => DEPARTMENTS.find((d) => d.id === id);

export const getAllPositions = () => {
  const positions = [];
  EXECUTIVE_TEAM.positions.forEach((p) => {
    positions.push({ ...p, department: "Executive Office", departmentId: "executive" });
  });
  DEPARTMENTS.forEach((dept) => {
    dept.positions.forEach((p) => {
      positions.push({ ...p, department: dept.title, departmentId: dept.id });
    });
  });
  return positions;
};

